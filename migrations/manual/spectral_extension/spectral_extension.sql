-------------Trigger: trg_check_result_value_derived, Trigger function: core.check_value_result_spectral_derived_specimen()---------------------------


DROP TRIGGER IF EXISTS trg_check_result_value_derived ON core.result_spectral_derived_specimen;

DROP FUNCTION IF EXISTS core.check_result_value_derived();

CREATE OR REPLACE FUNCTION core.check_value_result_spectral_derived_specimen()
    RETURNS trigger
    LANGUAGE 'plpgsql'
    COST 100
    VOLATILE NOT LEAKPROOF
AS $BODY$
DECLARE
    observation core.observation_phys_chem_specimen%ROWTYPE;
    spectral_specimen_specimen_id INTEGER;
BEGIN
    ---- First check for the value bounds ----
    -- Retrieve the physio-chemical observation record
    SELECT *
      INTO observation
      FROM core.observation_phys_chem_specimen p,
           core.observation_spectral_derived_specimen d
     WHERE p.observation_phys_chem_specimen_id = d.observation_phys_chem_specimen_id
       AND d.observation_spectral_derived_specimen_id = NEW.observation_spectral_derived_specimen_id;
    -- Check if the result value is within the admissable bounds
    IF NEW.value < observation.value_min OR NEW.value > observation.value_max THEN
        RAISE EXCEPTION 'Result value outside admissable bounds for the related physio-chemical observation.';
    END IF;
    ----- Second check for the specimen_id match ----
    -- Only perform the check if result_spectral_specimen_id is provided (not NULL)
    IF NEW.result_spectral_specimen_id IS NOT NULL THEN
        -- Retrieve the specimen_id from the result_spectral_specimen table
        SELECT specimen_id
        INTO spectral_specimen_specimen_id
        FROM core.result_spectral_specimen
        WHERE result_spectral_specimen_id = NEW.result_spectral_specimen_id;

        -- If no matching record is found or the specimen_id does not match, raise an error
        IF spectral_specimen_specimen_id IS NULL THEN
            RAISE EXCEPTION 'No matching result_spectral_specimen_id found in core.result_spectral_specimen';
        ELSIF spectral_specimen_specimen_id <> NEW.specimen_id THEN
            RAISE EXCEPTION 'Specimen ID mismatch: % (expected) vs. % (provided)',
                spectral_specimen_specimen_id, NEW.specimen_id;
        END IF;
    END IF;

    RETURN NEW;
END;
$BODY$;

COMMENT ON FUNCTION core.check_value_result_spectral_derived_specimen
    IS 'Check if the new inserting derived result value is within the bounds and if the specimen id of the new inserting derived result and the related spectrum result match.';

CREATE OR REPLACE TRIGGER trg_check_value_result_spectral_derived_specimen
    BEFORE INSERT OR UPDATE
    ON core.result_spectral_derived_specimen
    FOR EACH ROW
    EXECUTE FUNCTION core.check_value_result_spectral_derived_specimen();

COMMENT ON TRIGGER trg_check_value_result_spectral_derived_specimen ON core.result_spectral_derived_specimen
    IS 'Verifies if the value assigned to the result is valid. See the function core.check_value_result_spectral_derived_specimen() function for implementation.';


-----------------------------ON DELETE CASCADE for spectral tables---------------------------------------------------
-- This section ensures that when a specimen is deleted, related spectral data is also deleted.
-- Uses dynamic constraint replacement to handle both fresh installs and updates to existing tables.

-- result_spectral_specimen.specimen_id -> specimen (CASCADE)
-- Note: Spectral extension uses inline REFERENCES, need to find auto-generated constraint name
DO $$
DECLARE
    constraint_name text;
BEGIN
    -- Find and drop any existing FK constraint on specimen_id (could be auto-named or fk_specimen)
    SELECT conname INTO constraint_name
    FROM pg_constraint
    WHERE conrelid = 'core.result_spectral_specimen'::regclass
      AND contype = 'f'
      AND EXISTS (
          SELECT 1 FROM unnest(conkey) AS k
          JOIN pg_attribute a ON a.attrelid = conrelid AND a.attnum = k
          WHERE a.attname = 'specimen_id'
      );

    IF constraint_name IS NOT NULL THEN
        EXECUTE format('ALTER TABLE core.result_spectral_specimen DROP CONSTRAINT %I', constraint_name);
    END IF;

    -- Add the constraint with CASCADE
    ALTER TABLE core.result_spectral_specimen
        ADD CONSTRAINT fk_specimen FOREIGN KEY (specimen_id) REFERENCES core.specimen(specimen_id) ON DELETE CASCADE;
END $$;

-- result_spectral_derived_specimen.specimen_id -> specimen (CASCADE)
DO $$
DECLARE
    constraint_name text;
BEGIN
    -- Find and drop any existing FK constraint on specimen_id
    SELECT conname INTO constraint_name
    FROM pg_constraint
    WHERE conrelid = 'core.result_spectral_derived_specimen'::regclass
      AND contype = 'f'
      AND EXISTS (
          SELECT 1 FROM unnest(conkey) AS k
          JOIN pg_attribute a ON a.attrelid = conrelid AND a.attnum = k
          WHERE a.attname = 'specimen_id'
      );

    IF constraint_name IS NOT NULL THEN
        EXECUTE format('ALTER TABLE core.result_spectral_derived_specimen DROP CONSTRAINT %I', constraint_name);
    END IF;

    -- Add the constraint with CASCADE
    ALTER TABLE core.result_spectral_derived_specimen
        ADD CONSTRAINT fk_specimen FOREIGN KEY (specimen_id) REFERENCES core.specimen(specimen_id) ON DELETE CASCADE;
END $$;


