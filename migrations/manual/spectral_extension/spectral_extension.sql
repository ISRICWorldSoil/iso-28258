-----------------------------ETL FUNCTION: core.etl_bulk_insert_spectral_derived_specimen_results---------------------------------------------------
-- Bulk insert spectral derived results for specimens
-- Used by the bulk ETL process for high-performance batch inserts

DROP FUNCTION IF EXISTS core.etl_bulk_insert_spectral_derived_specimen_results(jsonb);
CREATE OR REPLACE FUNCTION core.etl_bulk_insert_spectral_derived_specimen_results(p_data jsonb)
RETURNS TABLE(inserted_count integer, skipped_count integer)
LANGUAGE plpgsql
AS $$
DECLARE
    v_inserted INT := 0;
    v_total INT := 0;
BEGIN
    -- Insert results with observation lookup, filtering out-of-bounds values
    WITH input_data AS (
        SELECT
            (r->>'specimen_id')::INT AS spec_id,
            (r->>'property_uri')::TEXT AS prop_uri,
            (r->>'procedure_uri')::TEXT AS proc_uri,
            (r->>'unit_uri')::TEXT AS u_uri,
            (r->>'model')::TEXT AS model_name,
            (r->>'organisation_name')::TEXT AS org_name,
            (r->>'value')::NUMERIC AS val
        FROM jsonb_array_elements(p_data) AS r
    ),
    input_with_observation AS (
        SELECT
            d.spec_id,
            d.val,
            osd.observation_spectral_derived_specimen_id AS obs_id,
            opc.value_min,
            opc.value_max,
            org.organisation_id AS org_id
        FROM input_data d
        -- Find the observation_phys_chem_specimen based on property/procedure/unit
        JOIN core.observation_phys_chem_specimen opc ON
            opc.property_phys_chem_id = (
                SELECT property_phys_chem_id
                FROM core.property_phys_chem
                WHERE uri ILIKE '%' || d.prop_uri
                LIMIT 1
            )
            AND opc.procedure_phys_chem_id = (
                SELECT procedure_phys_chem_id
                FROM core.procedure_phys_chem
                WHERE uri ILIKE '%' || d.proc_uri
                LIMIT 1
            )
            AND opc.unit_of_measure_id = (
                SELECT unit_of_measure_id
                FROM core.unit_of_measure
                WHERE uri ILIKE '%' || d.u_uri
                LIMIT 1
            )
        -- Find the spectral model
        JOIN core.model_spectral ms ON
            ms.name ILIKE '%' || d.model_name || '%'
        -- Find the observation_spectral_derived_specimen linking model to observation
        JOIN core.observation_spectral_derived_specimen osd ON
            osd.model_spectral_id = ms.model_spectral_id
            AND osd.observation_phys_chem_specimen_id = opc.observation_phys_chem_specimen_id
        -- Get organisation_id (optional)
        LEFT JOIN metadata.organisation org ON
            org.name ILIKE '%' || d.org_name || '%'
    ),
    -- Filter to only valid values (within bounds)
    valid_data AS (
        SELECT d.*
        FROM input_with_observation d
        WHERE (d.value_min IS NULL OR d.val >= d.value_min)
          AND (d.value_max IS NULL OR d.val <= d.value_max)
    ),
    inserted AS (
        INSERT INTO core.result_spectral_derived_specimen (
            observation_spectral_derived_specimen_id,
            specimen_id,
            organisation_id,
            result_spectral_specimen_id,
            value
        )
        SELECT
            d.obs_id,
            d.spec_id,
            d.org_id,
            NULL,  -- result_spectral_specimen_id is optional
            d.val
        FROM valid_data d
        ON CONFLICT DO NOTHING
        RETURNING 1
    )
    SELECT COUNT(*)::INT INTO v_inserted FROM inserted;

    SELECT jsonb_array_length(p_data)::INT INTO v_total;

    RETURN QUERY SELECT v_inserted, (v_total - v_inserted)::INT;
END;
$$;

COMMENT ON FUNCTION core.etl_bulk_insert_spectral_derived_specimen_results(p_data jsonb)
IS 'Bulk insert spectral derived results for specimens. Input is JSONB array with specimen_id, property_uri, procedure_uri, unit_uri, model, organisation_name, value.';


