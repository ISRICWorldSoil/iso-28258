-------------------- Core.Sensor ---------------------------
CREATE TABLE IF NOT EXISTS core.sensor
(
    model character varying NOT NULL,
    make character varying,
    uri character varying,
    sensor_id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    resolution real,
    zero_filling_factor integer,
    number_points integer,
    laser_wavenumber numeric,
    wavenumber_one numeric,
    wavenumber_last numeric
);

COMMENT ON TABLE core.sensor
    IS 'An instrument used to perform spectral observations on soil specimens or directly in a soil pit. An instrument can carry more than one sensor, each operating in different bands of the spectrum. The table observation_sensor provides the association between instrument and spectrum bands. The term sensor is retained for alignment with SOSA/SSN.';

COMMENT ON COLUMN core.sensor.model
    IS 'Model of this spectral instrument.';

COMMENT ON COLUMN core.sensor.make
    IS 'Make or brand of this spectral instrument.';

COMMENT ON COLUMN core.sensor.uri
    IS 'URI to an external description of the spectral instrument.';

COMMENT ON COLUMN core.sensor.sensor_id
    IS 'Synthetic primary key.';

COMMENT ON COLUMN core.sensor.resolution
    IS 'Spectral resolution';

COMMENT ON COLUMN core.sensor.zero_filling_factor
    IS 'Interpolation mode (fourier transform) to increase the digital resolution of the spectrum.';

COMMENT ON COLUMN core.sensor.number_points
    IS 'Number of wavelength intervals forming the spectrum.';

COMMENT ON COLUMN core.sensor.laser_wavenumber
    IS 'Light source or reference wavelenght.';

COMMENT ON COLUMN core.sensor.wavenumber_one
    IS 'First wavelength analysed.';

COMMENT ON COLUMN core.sensor.wavenumber_last
    IS 'Last wavelength analysed.';


